// Objects keys and values
const countries = { MX: 'Mexico', CO: 'Colombia', AR: 'Argentina', PE: 'Peru', EC: 'Ecuador' };

// Object.keys()
console.log(Object.keys(countries)); // ['MX', 'CO', 'AR', 'PE', 'EC']
// Object.values()
console.log(Object.values(countries)); // ['Mexico', 'Colombia', 'Argentina', 'Peru', 'Ecuador']